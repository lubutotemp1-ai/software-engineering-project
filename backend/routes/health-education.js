const express = require('express');
const router = express.Router();
const axios = require('axios');
const db = require('../db/database');
const authMiddleware = require('../middleware/auth');
const { checkAIUsageLimit, recordAIUsage } = require('../middleware/aiLimits');

router.use(authMiddleware);

// POST /api/health-education/generate - Get AI health education (with usage limits)
router.post('/generate', checkAIUsageLimit, async (req, res) => {
  try {
    const { topic, difficulty_level } = req.body;

    if (!topic || topic.trim() === '') {
      return res.status(400).json({ error: 'Health topic is required.' });
    }

    const prompt = `You are a health education specialist providing clear, accurate, and engaging educational content about health topics.

Topic: ${topic}
Difficulty Level: ${difficulty_level || 'intermediate'} (beginner, intermediate, or advanced)

Please provide a comprehensive health education article about this topic. Structure your response as follows:

Overview: A brief introduction to the topic and why it's important.

Key Concepts: 3-5 important concepts related to this health topic with clear explanations.

Common Misconceptions: 2-3 myths or misconceptions about this topic and the truth.

Prevention and Wellness: Practical tips for maintaining health related to this topic.

When to Seek Medical Help: Signs or symptoms that warrant professional medical attention.

Resources: Suggestions for further learning or professional support.

Disclaimer: Remind readers that this educational content is not a substitute for professional medical advice.`;

    // Call OpenRouter API
    const response = await axios.post(
      'https://openrouter.ai/api/v1/chat/completions',
      {
        model: 'meta-llama/llama-2-70b-chat',
        messages: [{ role: 'user', content: prompt }],
        max_tokens: 1500,
      },
      {
        headers: {
          'Authorization': `Bearer ${process.env.OPENROUTER_API_KEY}`,
          'HTTP-Referer': process.env.FRONTEND_URL || 'http://localhost:3000',
          'X-Title': 'Health Easy Portal',
        },
      }
    );

    const educationText = response.data.choices[0].message.content;

    // Save to database
    const result = await db.run_(
      `INSERT INTO education_materials (title, description, content, category)
       VALUES ($1, $2, $3, $4) RETURNING id`,
      [topic, `Generated by AI on ${new Date().toLocaleDateString()}`, educationText, 'ai-generated']
    );

    // Record AI usage
    await recordAIUsage(req.user.id, 'education');

    const savedMaterial = await db.get_(
      'SELECT * FROM education_materials WHERE id = $1',
      [result.lastInsertRowid]
    );

    res.json({
      material: savedMaterial,
      content: educationText,
      remaining_uses: req.aiLimit.remaining - 1,
      usage_info: req.aiLimit
    });

  } catch (err) {
    console.error('Health Education error:', err.message);
    res.status(500).json({ error: err.message || 'Failed to generate health education content.' });
  }
});

// GET /api/health-education/materials - Get all health education materials
router.get('/materials', async (req, res) => {
  try {
    const materials = await db.all_(
      `SELECT * FROM education_materials ORDER BY created_at DESC LIMIT 50`
    );
    res.json(materials);
  } catch (err) {
    console.error('Fetch materials error:', err.message);
    res.status(500).json({ error: 'Failed to fetch education materials.' });
  }
});

// GET /api/health-education/materials/:id - Get specific education material
router.get('/materials/:id', async (req, res) => {
  try {
    const material = await db.get_(
      `SELECT * FROM education_materials WHERE id = $1`,
      [req.params.id]
    );
    if (!material) {
      return res.status(404).json({ error: 'Material not found.' });
    }
    res.json(material);
  } catch (err) {
    console.error('Fetch material error:', err.message);
    res.status(500).json({ error: 'Failed to fetch material.' });
  }
});

// DELETE /api/health-education/materials/:id - Delete education material (admin only)
router.delete('/materials/:id', async (req, res) => {
  if (req.user.role !== 'admin') {
    return res.status(403).json({ error: 'Admin access required.' });
  }
  try {
    const material = await db.get_(
      `SELECT * FROM education_materials WHERE id = $1`,
      [req.params.id]
    );
    if (!material) {
      return res.status(404).json({ error: 'Material not found.' });
    }
    await db.query('DELETE FROM education_materials WHERE id = $1', [req.params.id]);
    res.json({ message: 'Material deleted.' });
  } catch (err) {
    console.error('Delete material error:', err.message);
    res.status(500).json({ error: 'Failed to delete material.' });
  }
});

module.exports = router;
